--OR

CREATE TRIGGER FINALIAMARKS
BEFORE INSERT
ON IAMARKS
FOR EACH ROW
SET NEW.FINAL=(
(NEW.TEST1+NEW.TEST2+NEW.TEST3)
-LEAST(NEW.TEST1, NEW.TEST2, NEW.TEST3))/2;


--5
SELECT I.USN, S.SEM, S.SECTION, I.SUBCODE,
(CASE
WHEN FINAL BETWEEN 17 AND 20 THEN 'OUTSTANDING'
WHEN FINAL BETWEEN 12 AND 16 THEN 'AVERAGE'
ELSE 'WEAK'
END) AS CATEGORY
FROM IAMARKS I, CLASS C, SEMSEC S
WHERE I.USN=C.USN AND C.SSID=S.SSID
AND S.SEM=8 AND S.SECTION IN ('A', 'B', 'C');