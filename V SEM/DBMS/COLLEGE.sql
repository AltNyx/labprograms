--1
SELECT STUDENT.*
FROM STUDENT NATURAL JOIN
(CLASS NATURAL JOIN SEMSEC)
WHERE SEM=4 AND SECTION='C';

--OR

SELECT STUDENT.*
FROM STUDENT, SEMSEC, CLASS
WHERE SEMSEC.SSID=CLASS.SSID
AND CLASS.USN=STUDENT.USN
AND SEMSEC.SEM=4
AND SEMSEC.SECTION='C';


--2
SELECT SEM, SECTION, GENDER, COUNT(*)
FROM STUDENT NATURAL JOIN
(CLASS NATURAL JOIN SEMSEC)
GROUP BY SEM, SECTION, GENDER;

--OR

SELECT SEM, SECTION, GENDER, COUNT(*)
FROM SEMSEC SS, CLASS C, STUDENT S
WHERE SS.SSID=C.SSID AND S.USN=C.USN
GROUP BY SEM, SECTION, GENDER;


