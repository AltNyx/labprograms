	AREA CODE1, CODE, READONLY
ENTRY
	LDR R0, =INPUT
	LDR R1, [R0]
	MOV R2, #9; 10 ELEMENTS
	MOV R3, R1; R3 MIN
	MOV R4, R1; R4 MAX
REPEAT ADD R0, #4
	LDR R5, [R0]
	CMP R3, R5
	MOVGT R3, R5; MOVE IF R3 IS GREATER
	CMP R4, R5
	MOVLT R4, R5; MOVE IF R4 IS LESS
	SUBS R2, R2, #1
	CMP R2, #0
	BNE REPEAT
	LDR R6, =SMALLEST
	STR R3, [R6]
	LDR R6, =LARGEST
	STR R4, [R6]
STOP B STOP
INPUT DCD 6, 5, 7, 8, 4, 3, 9, 10, 2, 1
	AREA DATA1, DATA, READWRITE
SMALLEST DCD 0
LARGEST DCD 0
	END